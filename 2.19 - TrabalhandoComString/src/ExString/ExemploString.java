package ExString;

public class ExemploString {

	public static void main (String [] args) {
		System.out.println("Fala Mergulhador Java");
		
		int x = 10;
		int y = 2; 
		
		
		// Concatenando String por conta disso o resultado é 102
		System.out.println("Resultado: " + x+y );
		
		// Comando os valores inteiros e não concatenando como antes. 
		System.out.println("Resultado: " + (x+y) );
		
		// Declaração de Variaveis String: 
		
		String nome = "Priscila";
		int idade = 31;
		
		System.out.println("\nMeu nome é: " + nome + " e tenho " + idade);
		
		
	}
	
	
	
}
