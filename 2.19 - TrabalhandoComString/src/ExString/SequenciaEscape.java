package ExString;

public class SequenciaEscape {
	public static void main (String [] args) {
		
		// imprimir "Priscila" com aspas 
		System.out.println("Oi \"Priscila\"");
		
		// pular linha 
		System.out.println("Seu nome é: \nJoão");
		
		// Caminho de Diretorio,o \ é leito como um caracter de escape
		System.out.println("C:\\\\Program Files");
	}
}
